#include <stdio.h>
#include <stdlib.h>
#include <time.h>

int init_seed() {
    srand((unsigned) time(NULL));
    return 0;
}

void draw_barrier() {
    static int begin = 0;
    if (begin == 0) {
        init_seed();
        begin == 1;
    }

    int t = rand() % 4;
    puts("");
    if (t == 0)
        puts("+----------------------------------------+");
    
    if (t == 1)
        puts("=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-==-=-=-=");

    if (t == 2)
        puts("::::::::::::::::::::::::::::::::::::::::::");

    if (t == 3)
        puts("\\_/^\\_/^\\_/^\\_/^\\_/^\\_/^\\_/^\\_/^\\_/^\\_/^\\");
}

